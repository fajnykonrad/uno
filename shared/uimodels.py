COLORS = {
    "r": "red",
    "g": "green",
    "y": "yellow",
    "b": "blue",
    None: "magenta"  
}
def print_card (card):
    output = ""
    for line in CARD_ART[card['value']]:
        output += f"[{COLORS[card['color']]}]{line}[/]\n"
    return output

def print_hand(hand, selected_index=None):
    output_lines = ["."] * 10
    if selected_index is not None:
        for idx, card in enumerate(hand):
            color = COLORS[card["color"]]

            if idx == selected_index:
                art = CARD_ART[card["value"]]
                offset = 0
            elif idx < selected_index:
                art = CARD_ART["l" + card["value"]]
                offset = 1
            else:
                art = CARD_ART["r" + card["value"]]
                offset = 1

            for i in range(9):
                output_lines[i + offset] += f"[{color}]{art[i]}[/] "
            if offset == 1:
                output_lines[0] += "\u0020" * 3
            else:
                output_lines[9] += "\u0020" * 12
    else:
        for idx, card in enumerate(hand):
            color = COLORS[card["color"]]
            art = CARD_ART[card["value"]] if idx == len(hand) - 1 else CARD_ART["l" + card["value"]]

            for i in range(9):
                output_lines[i + 1] += f"[{color}]{art[i]}[/] "
            if offset == 1:
                output_lines[0] += "\u0020" * 3
            else:
                output_lines[9] += "\u0020" * 12
    for i in range(10):
        output_lines[i] += "."
    return "\n".join(output_lines)
