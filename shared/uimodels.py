COLORS = {
    "r": "red",
    "g": "green",
    "y": "yellow",
    "b": "blue",
    None: "magenta"  
}
def print_card (card):
    output = ""
    for line in CARD_ART[card['value']]:
        output += f"[{COLORS[card['color']]}]{line}[/]\n"
    return output
def print_hand (hand, selected_index=None):
    output_lines = [""] * 9
    for card in hand:
        if selected_index is not None and (hand.index(card) == selected_index or hand.index(card) == len(hand)-1):
            art = CARD_ART[card['value']]
        else:
            art = CARD_ART["h" + card['value']]
        color = COLORS[card['color']]
        for i in range(9):
            output_lines[i] += f"[{color}]{art[i]}[/] "
    return "\n".join(output_lines)